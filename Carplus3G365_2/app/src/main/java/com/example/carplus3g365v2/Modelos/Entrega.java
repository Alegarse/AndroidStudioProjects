package com.example.carplus3g365v2.Modelos;

public class Entrega {

    private String Suc, Hora, Gp, Gp2, Dias;
    private String Mtr, Cliente, Lugar, Obs;

    public Entrega(String suc, String hora, String gp, String gp2, String dias, String mtr, String cliente, String lugar, String obs) {
        Suc = suc;
        Hora = hora;
        Gp = gp;
        Gp2 = gp2;
        Dias = dias;
        Mtr = mtr;
        Cliente = cliente;
        Lugar = lugar;
        Obs = obs;
    }

    public String getSuc() {
        return Suc;
    }

    public void setSuc(String suc) {
        Suc = suc;
    }

    public String getHora() {
        return Hora;
    }

    public void setHora(String hora) {
        Hora = hora;
    }

    public String getGp() {
        return Gp;
    }

    public void setGp(String gp) {
        Gp = gp;
    }

    public String getGp2() {
        return Gp2;
    }

    public void setGp2(String gp2) {
        Gp2 = gp2;
    }

    public String getDias() {
        return Dias;
    }

    public void setDias(String dias) {
        Dias = dias;
    }

    public String getMtr() {
        return Mtr;
    }

    public void setMtr(String mtr) {
        Mtr = mtr;
    }

    public String getCliente() {
        return Cliente;
    }

    public void setCliente(String cliente) {
        Cliente = cliente;
    }

    public String getLugar() {
        return Lugar;
    }

    public void setLugar(String lugar) {
        Lugar = lugar;
    }

    public String getObs() {
        return Obs;
    }

    public void setObs(String obs) {
        Obs = obs;
    }
}
