package com.example.carplus3g365v2.Modelos;

import android.widget.EditText;

public class Utils {

    public static void enableDisableEditText(boolean isEnabled, EditText txt){
        txt.setFocusable(isEnabled);
        txt.setFocusableInTouchMode(isEnabled) ;
        txt.setClickable(isEnabled);
        txt.setLongClickable(isEnabled);
        txt.setCursorVisible(isEnabled) ;
    }
}
